// Function to get sample data
function hot_news_get_sample_data($type = 'all')
{
    global $sample_news_data, $sample_categories, $sample_contact_info,
    $sample_menu_items, $sample_footer_links, $sample_newsletter,
    $sample_tags, $sample_read_more;

    switch ($type) {
        case 'news':
            return $sample_news_data;
        case 'categories':
            return $sample_categories;
        case 'contact':
            return $sample_contact_info;
        case 'menu':
            return $sample_menu_items;
        case 'footer':
            return $sample_footer_links;
        case 'newsletter':
            return $sample_newsletter;
        case 'tags':
            return $sample_tags;
        case 'read_more':
            return $sample_read_more;
        default:
            return array(
                'news' => $sample_news_data,
                'categories' => $sample_categories,
                'contact' => $sample_contact_info,
                'menu' => $sample_menu_items,
                'footer' => $sample_footer_links,
                'newsletter' => $sample_newsletter,
                'tags' => $sample_tags,
                'read_more' => $sample_read_more
            );
    }
}

// Function to create sample posts (for development/demo purposes)
function hot_news_create_sample_posts()
{
    if (!current_user_can('manage_options')) {
        return false;
    }

    $sample_data = hot_news_get_sample_data('news');
    $created_posts = array();

    foreach ($sample_data as $data) {
        // Check if post already exists
        $existing_post = get_page_by_title($data['title'], OBJECT, 'post');
        if ($existing_post) {
            // Update existing post with new data
            $post_id = $existing_post->ID;
        } else {
            // Create category if it doesn't exist
            $category = get_category_by_slug(sanitize_title($data['category']));
            if (!$category) {
                $category_id = wp_create_category($data['category']);
            } else {
                $category_id = $category->term_id;
            }

            // Create post
            $post_data = array(
                'post_title' => $data['title'],
                'post_content' => $data['content'],
                'post_status' => 'publish',
                'post_type' => 'post',
                'post_category' => array($category_id),
                'post_date' => date('Y-m-d H:i:s', strtotime('-' . rand(1, 30) . ' days'))
            );

            $post_id = wp_insert_post($post_data);
        }

        if ($post_id && !is_wp_error($post_id)) {
            $created_posts[] = $post_id;

            // Set custom meta data
            if (isset($data['views'])) {
                update_post_meta($post_id, '_post_views', $data['views']);
            }
            if (isset($data['likes'])) {
                update_post_meta($post_id, '_post_likes', $data['likes']);
            }
            if (isset($data['dislikes'])) {
                update_post_meta($post_id, '_post_dislikes', $data['dislikes']);
            }
            if (isset($data['featured']) && $data['featured']) {
                update_post_meta($post_id, '_featured_post', '1');
            }
            if (isset($data['hot_news']) && $data['hot_news']) {
                update_post_meta($post_id, '_hot_news', '1');
            }

            // Add tags if available
            if (isset($data['tags']) && is_array($data['tags'])) {
                wp_set_post_tags($post_id, $data['tags']);
            }

            // Set featured image if available
            if (isset($data['image'])) {
                $image_path = get_template_directory() . '/assets/images/' . $data['image'];
                if (file_exists($image_path)) {
                    // This is a placeholder - in real implementation, you'd upload the image
                    // For now, we'll just store the image name as meta
                    update_post_meta($post_id, '_sample_image', $data['image']);
                }
            }
        }
    }

    return $created_posts;
}

// Function to create sample pages
function hot_news_create_sample_pages()
{
    if (!current_user_can('manage_options')) {
        return false;
    }

    global $sample_pages_data;
    $created_pages = array();

    foreach ($sample_pages_data as $page_data) {
        // Check if page already exists
        $existing_page = get_page_by_title($page_data['title'], OBJECT, 'page');
        if ($existing_page) {
            $page_id = $existing_page->ID;
        } else {
            // Create page
            $page_args = array(
                'post_title' => $page_data['title'],
                'post_content' => $page_data['content'],
                'post_status' => 'publish',
                'post_type' => 'page',
                'post_name' => $page_data['slug']
            );

            $page_id = wp_insert_post($page_args);
        }

        if ($page_id && !is_wp_error($page_id)) {
            $created_pages[] = $page_id;

            // Set page template if specified
            if (isset($page_data['template'])) {
                update_post_meta($page_id, '_wp_page_template', $page_data['template']);
            }
        }
    }

    return $created_pages;
}

// Function to create main navigation menu
function hot_news_create_main_menu()
{
    if (!current_user_can('manage_options')) {
        return false;
    }

    global $sample_menu_items;

    // Create menu
    $menu_name = 'Menu chính';
    $menu_exists = wp_get_nav_menu_object($menu_name);

    if (!$menu_exists) {
        $menu_id = wp_create_nav_menu($menu_name);
    } else {
        $menu_id = $menu_exists->term_id;
        // Clear existing menu items
        $menu_items = wp_get_nav_menu_items($menu_id);
        if ($menu_items) {
            foreach ($menu_items as $menu_item) {
                wp_delete_post($menu_item->ID, true);
            }
        }
    }

    if ($menu_id && !is_wp_error($menu_id)) {
        $menu_order = 1;

        foreach ($sample_menu_items as $title => $url) {
            if ($url === home_url('/')) {
                // Home page
                wp_update_nav_menu_item($menu_id, 0, array(
                    'menu-item-title' => $title,
                    'menu-item-url' => $url,
                    'menu-item-status' => 'publish',
                    'menu-item-position' => $menu_order++
                ));
            } elseif ($url === '#hot-news') {
                // Hot news - link to category or custom page
                wp_update_nav_menu_item($menu_id, 0, array(
                    'menu-item-title' => $title,
                    'menu-item-url' => home_url('/?hot_news=1'),
                    'menu-item-status' => 'publish',
                    'menu-item-position' => $menu_order++
                ));
            } elseif ($url === '#contact') {
                // Contact page
                $contact_page = get_page_by_title('Liên hệ');
                if ($contact_page) {
                    wp_update_nav_menu_item($menu_id, 0, array(
                        'menu-item-title' => $title,
                        'menu-item-object' => 'page',
                        'menu-item-object-id' => $contact_page->ID,
                        'menu-item-type' => 'post_type',
                        'menu-item-status' => 'publish',
                        'menu-item-position' => $menu_order++
                    ));
                }
            }
        }

        // Set as primary menu
        $locations = get_theme_mod('nav_menu_locations');
        $locations['primary'] = $menu_id;
        set_theme_mod('nav_menu_locations', $locations);

        return $menu_id;
    }

    return false;
}

// Function to setup sample theme options
function hot_news_setup_sample_theme_options()
{
    if (!current_user_can('manage_options')) {
        return false;
    }

    $contact_info = hot_news_get_sample_data('contact');

    // Set theme customizer options
    set_theme_mod('hot_news_contact_email', $contact_info['email']);
    set_theme_mod('hot_news_contact_phone', $contact_info['phone']);

    // Set social links
    foreach ($contact_info['social_links'] as $platform => $url) {
        set_theme_mod('hot_news_social_' . $platform, $url);
    }

    // Set featured categories
    $categories = array_keys(hot_news_get_sample_data('categories'));
    if (count($categories) >= 2) {
        set_theme_mod('hot_news_featured_category_1', sanitize_title($categories[0]));
        set_theme_mod('hot_news_featured_category_2', sanitize_title($categories[1]));
    }

    return true;
}
